const frutas = ["Banana", "Morango", "Uva", "Abacaxi"];
console.log(frutas);
frutas.push("Melão");
console.log(frutas)
frutas.shift() // Remove o primeiro elemento do array
console.log(frutas);
frutas.unshift("Banana") // Retorna o elemento no primeiro índice
console.log(frutas);
frutas.pop();
console.log(frutas) // Remove o último elemento do array